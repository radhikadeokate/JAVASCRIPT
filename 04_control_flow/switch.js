const month ="March"
switch (month) {
    case "January":
        console.log("January");
         break;
    case 2:
        console.log("Feb");
         break;
    case "March":
        console.log("March");
         break;
    case 4:
        console.log("April");
         break;

    default:
        console.log("Default case match ");
        break;
}